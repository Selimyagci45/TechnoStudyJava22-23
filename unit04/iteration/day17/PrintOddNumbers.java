package unit04.iteration.day17;

public class PrintOddNumbers {
    public static void main(String[] args) {
        for (int i = 2; i <= 10; i = i + 2) {
            System.out.println("i: " + i);
        }



        /*
         * Print odd numbers starting from 1 until 10
         * use for loop
         * */
    }
}

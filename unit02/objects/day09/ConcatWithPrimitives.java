package unit02.objects.day09;

public class ConcatWithPrimitives {
    public static void main(String[] args) {
        System.out.println("There are " + 70 + 5 + " students.");
        System.out.println(70 + 5 + " students in the class" + 11 + 33);
        System.out.println("There are " + (70 + 5) + " students.");
    }
}

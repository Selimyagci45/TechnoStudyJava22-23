package unit01.primitive.types.day02;

public class LiteralVsStringLiteral {

    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 7;

        System.out.println(num1 + num2);

        String str1 = "5";
        String str2 = "7";

        System.out.println(str1+str2);
    }

}

